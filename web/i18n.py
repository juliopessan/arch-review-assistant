def get_t(lang: str = "en"):
    """Return a translation function for the given language."""
    def t(key: str, **kwargs) -> str:
        row = TRANSLATIONS.get(key, {})
        text = row.get(lang, row.get("en", key))
        return text.format(**kwargs) if kwargs else text
    return t
